import * as fs from 'fs';
import * as path from 'path';
import * as os from 'os';
import { SagaAlert } from './saga_alert';
import { SagaHealthSignal } from './saga_monitor';

const BISHOP_DROPZONE_PATH = path.join(
  os.homedir(),
  '.claude',
  'state',
  'bishop_dropzone_inbox'
);

/**
 * Notify the founder about a saga health alert by writing to Bishop dropzone
 */
export function notifyFounder(alert: SagaAlert, signal: SagaHealthSignal): void {
  try {
    // Ensure the dropzone directory exists
    if (!fs.existsSync(BISHOP_DROPZONE_PATH)) {
      fs.mkdirSync(BISHOP_DROPZONE_PATH, { recursive: true });
    }

    const alertFilename = `SAGA_HEALTH_ALERT_${alert.saga_id}.md`;
    const alertFilePath = path.join(BISHOP_DROPZONE_PATH, alertFilename);

    const alertContent = generateAlertMarkdown(alert, signal);

    fs.writeFileSync(alertFilePath, alertContent, 'utf-8');

    console.log(`[WATCHDOG] Alert notification written to: ${alertFilePath}`);
  } catch (error) {
    console.error('[WATCHDOG] Failed to notify founder:', error);
    throw new Error(`Failed to write alert notification: ${error}`);
  }
}

