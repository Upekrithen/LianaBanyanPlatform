// ─── electron/migrations.ts ───────────────────────────────────────────────────

export const ELECTRON_MIGRATIONS = `// {{APP_NAME}} — DB Migrations
// Generated by Hearth App Builder (B69)

import Database from 'better-sqlite3';
import { readFileSync, existsSync } from 'fs';
import { join } from 'path';

const MIGRATION_FILES = [
  join(__dirname, '../../db/migrations/0001_init.sql'),
];

export function runMigrations(db: Database.Database): void {
  db.exec(\`CREATE TABLE IF NOT EXISTS __migrations (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    filename TEXT NOT NULL UNIQUE,
    applied_at TEXT NOT NULL DEFAULT (datetime('now'))
  );\`);

  for (const file of MIGRATION_FILES) {
    const filename = file.split(/[\\\\/]/).pop() ?? file;
    const already = db.prepare('SELECT 1 FROM __migrations WHERE filename = ?').get(filename);
    if (already) continue;
    if (!existsSync(file)) {
      console.warn('[Migrations] File not found:', file);
      continue;
    }
    const sql = readFileSync(file, 'utf8');
    db.exec(sql);
    db.prepare('INSERT INTO __migrations (filename) VALUES (?)').run(filename);
    console.log('[Migrations] Applied:', filename);
  }
}
`;

