// Template vars use {{VAR_NAME}} syntax; render() replaces all occurrences.

export function render(template: string, vars: Record<string, string>): string {
  return template.replace(/\{\{([A-Z_]+)\}\}/g, (_, key) => vars[key] ?? `{{${key}}}`);
}

