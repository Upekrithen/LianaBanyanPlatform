/**
 * Record when a user selects a template
 */
export async function recordTemplateSelection(
  templateId: string,
  creatorId: string,
  userId: string,
  campaignId?: string
): Promise<string | null> {
  try {
    const { data, error } = await supabase
      .from('template_attribution')
      .insert({
        template_id: templateId,
        creator_id: creatorId,
        user_id: userId,
        campaign_id: campaignId || null,
        marks_for_selection: 1,
        total_marks_awarded: 1
      })
      .select('id')
      .single();

    if (error) throw error;
    return data.id;
  } catch (err) {
    console.error('Error recording template selection:', err);
    return null;
  }
}

/**
 * Record when a campaign using a template is sent
 */
export async function recordCampaignSent(
  attributionId: string
): Promise<boolean> {
  try {
    const { error } = await supabase
      .from('template_attribution')
      .update({
        sent_at: new Date().toISOString(),
        marks_for_send: 2,
        total_marks_awarded: supabase.rpc('get_attribution_total', { p_id: attributionId })
      })
      .eq('id', attributionId);

    if (error) throw error;
    return true;
  } catch (err) {
    console.error('Error recording campaign sent:', err);
    return false;
  }
}

/**
 * Record clicks generated by a template
 */
export async function recordClicks(
  attributionId: string,
  newClicks: number
): Promise<boolean> {
  try {
    // Get current clicks
    const { data: current, error: fetchError } = await supabase
      .from('template_attribution')
      .select('clicks_generated')
      .eq('id', attributionId)
      .single();

    if (fetchError) throw fetchError;

    const totalClicks = (current?.clicks_generated || 0) + newClicks;
    const marksForClicks = totalClicks * 0.1;

    const { error } = await supabase
      .from('template_attribution')
      .update({
        clicks_generated: totalClicks,
        marks_for_clicks: marksForClicks
      })
      .eq('id', attributionId);

    if (error) throw error;
    return true;
  } catch (err) {
    console.error('Error recording clicks:', err);
    return false;
  }
}

