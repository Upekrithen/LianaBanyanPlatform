/**
 * Create a campaign with research commitment
 */
export async function createCampaignWithCommitment(
  userId: string,
  name: string,
  templateIds: string[],
  researchCommitment: boolean,
  expirationHours: number,
  expirationType: string,
  projectId?: string
): Promise<string | null> {
  try {
    const { data, error } = await supabase
      .from('cue_card_campaigns')
      .insert({
        user_id: userId,
        project_id: projectId || null,
        name,
        template_ids: templateIds,
        research_commitment: researchCommitment,
        research_commitment_set_at: researchCommitment ? new Date().toISOString() : null,
        default_expiration_hours: expirationHours,
        expiration_type: expirationType,
        status: 'draft'
      })
      .select('id')
      .single();

    if (error) throw error;
    return data.id;
  } catch (err) {
    console.error('Error creating campaign:', err);
    return null;
  }
}

/**
 * Launch a campaign and satisfy commitment
 */
export async function launchCampaign(
  campaignId: string,
  userId: string
): Promise<boolean> {
  try {
    // Update campaign status
    const { error: campaignError } = await supabase
      .from('cue_card_campaigns')
      .update({
        status: 'active',
        launched_at: new Date().toISOString()
      })
      .eq('id', campaignId);

    if (campaignError) throw campaignError;

    // Satisfy commitment lock
    const { error: lockError } = await supabase
      .rpc('satisfy_commitment_lock', {
        p_user_id: userId,
        p_campaign_id: campaignId
      });

    if (lockError) {
      console.warn('No commitment lock to satisfy:', lockError);
    }

    return true;
  } catch (err) {
    console.error('Error launching campaign:', err);
    return false;
  }
}

/**
 * Complete a campaign and finalize all attributions
 */
export async function completeCampaign(
  campaignId: string
): Promise<{ totalMarksAwarded: number; attributionsFinalized: number }> {
  try {
    // Get campaign
    const { data: campaign, error: fetchError } = await supabase
      .from('cue_card_campaigns')
      .select('*')
      .eq('id', campaignId)
      .single();

    if (fetchError) throw fetchError;

    // Update campaign status
    const { error: updateError } = await supabase
      .from('cue_card_campaigns')
      .update({
        status: 'completed',
        completed_at: new Date().toISOString()
      })
      .eq('id', campaignId);

    if (updateError) throw updateError;

    // Finalize all attributions for this campaign
    const { data: attributions, error: attrError } = await supabase
      .from('template_attribution')
      .select('id')
      .eq('campaign_id', campaignId)
      .is('marks_paid_at', null);

    if (attrError) throw attrError;

    let totalMarks = 0;
    let finalized = 0;

    for (const attr of attributions || []) {
      const marks = await finalizeAttribution(attr.id);
      totalMarks += marks;
      finalized++;
    }

    // Update research pool aggregates if committed
    if (campaign.research_commitment && campaign.commitment_satisfied) {
      await supabase.rpc('update_research_pool_aggregates');
    }

    return {
      totalMarksAwarded: totalMarks,
      attributionsFinalized: finalized
    };
  } catch (err) {
    console.error('Error completing campaign:', err);
    return { totalMarksAwarded: 0, attributionsFinalized: 0 };
  }
}

