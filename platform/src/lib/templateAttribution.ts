/**
 * TEMPLATE ATTRIBUTION SERVICE
 * ============================
 * Tracks when templates are used and awards Marks to creators.
 * 
 * Marks Earned:
 * - Template selected: 1 Mark
 * - Campaign sent: 2 Marks
 * - Click generated: 0.1 Marks per click
 * - Conversion generated: 1 Mark per conversion
 * - Template copied (derivative): 5 Marks (highest honor)
 * 
 * Attribution Duration:
 * - Marks accrue for the duration of the campaign (typically 24-72 hours)
 * - After campaign ends, final tally is calculated
 * - Creator receives Marks as crystallized (not Shadow)
 */

import { supabase } from '@/integrations/supabase/client';

// ═══════════════════════════════════════════════════════════════════════════════
// TYPES
// ═══════════════════════════════════════════════════════════════════════════════

export interface Attribution {
  id: string;
  template_id: string;
  creator_id: string;
  user_id: string;
  campaign_id: string | null;
  selected_at: string;
  sent_at: string | null;
  clicks_generated: number;
  conversions_generated: number;
  marks_for_selection: number;
  marks_for_send: number;
  marks_for_clicks: number;
  marks_for_conversions: number;
  marks_for_derivative: number;
  total_marks_awarded: number;
  marks_paid_at: string | null;
  is_derivative: boolean;
}

export interface AttributionSummary {
  total_templates_used: number;
  total_marks_earned: number;
  total_clicks_generated: number;
  total_conversions_generated: number;
  top_templates: {
    template_id: string;
    template_title: string;
    uses: number;
    marks_earned: number;
  }[];
}

