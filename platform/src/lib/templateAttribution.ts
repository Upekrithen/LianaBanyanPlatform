/**
 * Finalize attribution and pay out marks to creator
 */
export async function finalizeAttribution(
  attributionId: string
): Promise<number> {
  try {
    const { data: marks, error } = await supabase
      .rpc('award_template_marks', { p_attribution_id: attributionId });

    if (error) throw error;
    return marks || 0;
  } catch (err) {
    console.error('Error finalizing attribution:', err);
    return 0;
  }
}

/**
 * Get attribution summary for a creator
 */
export async function getCreatorAttributionSummary(
  creatorId: string
): Promise<AttributionSummary | null> {
  try {
    const { data: attributions, error } = await supabase
      .from('template_attribution')
      .select(`
        *,
        cue_card_templates (
          id,
          title
        )
      `)
      .eq('creator_id', creatorId);

    if (error) throw error;
    if (!attributions || attributions.length === 0) {
      return {
        total_templates_used: 0,
        total_marks_earned: 0,
        total_clicks_generated: 0,
        total_conversions_generated: 0,
        top_templates: []
      };
    }

    // Aggregate by template
    const templateStats: Record<string, {
      template_id: string;
      template_title: string;
      uses: number;
      marks_earned: number;
    }> = {};

    let totalMarks = 0;
    let totalClicks = 0;
    let totalConversions = 0;

    attributions.forEach(attr => {
      const templateId = attr.template_id;
      const template = attr.cue_card_templates as { id: string; title: string } | null;
      
      if (!templateStats[templateId]) {
        templateStats[templateId] = {
          template_id: templateId,
          template_title: template?.title || 'Unknown',
          uses: 0,
          marks_earned: 0
        };
      }

      templateStats[templateId].uses++;
      templateStats[templateId].marks_earned += attr.total_marks_awarded || 0;
      totalMarks += attr.total_marks_awarded || 0;
      totalClicks += attr.clicks_generated || 0;
      totalConversions += attr.conversions_generated || 0;
    });

    const topTemplates = Object.values(templateStats)
      .sort((a, b) => b.marks_earned - a.marks_earned)
      .slice(0, 5);

    return {
      total_templates_used: Object.keys(templateStats).length,
      total_marks_earned: totalMarks,
      total_clicks_generated: totalClicks,
      total_conversions_generated: totalConversions,
      top_templates: topTemplates
    };
  } catch (err) {
    console.error('Error getting creator summary:', err);
    return null;
  }
}

/**
 * Get attribution summary for a user (templates they've used)
 */
export async function getUserAttributionHistory(
  userId: string
): Promise<Attribution[]> {
  try {
    const { data, error } = await supabase
      .from('template_attribution')
      .select('*')
      .eq('user_id', userId)
      .order('selected_at', { ascending: false });

    if (error) throw error;
    return (data as Attribution[]) || [];
  } catch (err) {
    console.error('Error getting user history:', err);
    return [];
  }
}

